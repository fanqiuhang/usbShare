package com.edata.bridge.usbshare;

import lombok.Data;

@Data
public class UsbshareDevice extends UsbsharePortBasic {

    /**
     * 虚拟usb端口号
     */
    protected String virtualUsbPort;

    /**
     * usb端口号
     */
    protected String usbPort;

    @Override
    public String toString() {
        return "UsbshareDevice{" +
                "virtualUsbPort='" + virtualUsbPort + '\'' +
                ", usbPort='" + usbPort + '\'' +
                ", busid='" + busid + '\'' +
                ", deviceType='" + deviceType + '\'' +
                ", deviceProtocolType='" + deviceProtocolType + '\'' +
                ", devicePID='" + devicePID + '\'' +
                ", deviceVID='" + deviceVID + '\'' +
                ", supportedNumber='" + supportedNumber + '\'' +
                ", deviceName='" + deviceName + '\'' +
                '}';
    }
}
